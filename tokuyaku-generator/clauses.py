def all_items():
    """全項目をフラットなリストで返す（カテゴリ名付き）。"""
    out = []
    for cat in CATEGORIES:
        for it in cat["items"]:
            out.append({
                "no": it["no"],
                "title": it["title"],
                "hint": it["hint"],
                "category": cat["name"],
                "category_no": cat["no"],
            })
    return out


def find_item(no: str):
    for it in all_items():
        if it["no"] == no:
            return it
    return None
