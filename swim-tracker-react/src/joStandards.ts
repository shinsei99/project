type TableKey = string;
const TABLE = new Map<TableKey, number>();

(function buildTable() {
  const genderIdx: Record<string, number> = { '男子': 0, '女子': 2 };
  const courseIdx: Record<string, number> = { '短水路 (25m)': 0, '長水路 (50m)': 1 };
  for (const [key, ageData] of Object.entries(RAW)) {
    const [event, distStr] = key.split('_');
    const distance = parseInt(distStr);
    for (const [ageCat, vals] of Object.entries(ageData)) {
      for (const [gender, gi] of Object.entries(genderIdx)) {
        for (const [course, ci] of Object.entries(courseIdx)) {
          const raw = vals[gi + ci];
          if (raw !== null) {
            TABLE.set(`${ageCat}|${gender}|${event}|${distance}|${course}`, t(raw));
          }
        }
      }
    }
  }
})();

export function ageCategoryFromBirthYear(birthYear: number): string {
  const today = new Date();
  const age = today.getFullYear() - birthYear;
  if (age <= 10) return '10歳以下';
  if (age <= 12) return '11〜12歳';
  if (age <= 14) return '13〜14歳';
  if (age <= 16) return '15〜16歳';
  return 'CS';
}

export function getJoTime(ageCat: string, gender: string, event: string, distance: number, course: string): number | null {
  return TABLE.get(`${ageCat}|${gender}|${event}|${distance}|${course}`) ?? null;
}
